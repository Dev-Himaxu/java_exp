package strClass;

public class Rplace {
 public static void main(String[] args) {
	String n = "Him";
	System.out.println(n.replace('H', 'T')); // it replaces the Prev Chara H with Chara T.
	 
	String s = "My name is Him";
	System.out.println(s.replace("Him", "Himanshu"));  //this will replace Him with Himanshu.
	
}
}
